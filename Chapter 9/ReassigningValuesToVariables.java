public class ReassigningValuesToVariables
{
    public static void main (String[] args)
    {
        double x = 2.0;
        double value;

        value = (3 * (x * x)) - (8 * x) + 4;

        System.out.println("At x =  " + x + " the value is " + value);
    }
}
